<br>
<h2>O que é?</h2>
<br>
<p>Paliativismo ou cuidados paliativos, como também pode ser chamado, são cuidados médicos especializados para adultos, idosos ou crianças que vivem com alguma condição incurável de saúde.<br><br>

Os cuidados paliativos visam melhorar a qualidade de vida dos pacientes, e de suas famílias, que enfrentam desafios associados a doenças com risco de vida, sejam elas físicas, psicológicas, sociais ou espirituais. Esses tratamentos são realizados por uma equipe de médicos, enfermeiros, técnicos em enfermagem, psicólogos, assistentes sociais, fisioterapeutas, terapeutas ocupacionais, nutricionistas, farmacêuticos e um representante espiritual.</p>
<br>
<h2>Para que servem os tratamentos?</h2><br>

<ul>
    <li id="list">Aliviar as dores e outros sintomas das doenças;</li>
    <li id="list">Gerenciar a dor e os efeitos colaterais do tratamento;</li>
    <li id="list">Melhorar o conforto e o bem-estar do paciente, com o fim de melhorar a qualidade de vida;</li>
    <li id="list">Aliviar o sofrimento;</li>
    <li id="list">Prevenir a ocorrência de novos problemas ou sintomas;</li>
    <li id="list">Afirmar a vida, considerando a morte como um processo natural;</li>
    <li id="list">Fornecer acompanhamentos humanizados até o fim da vida;</li>
    <li id="list">Manter a família informada sobre as condições dos pacientes e o que esperar;</li>
    <li id="list">Acompanhamento aos familiares para lidar com a perda e o luto;</li>
</ul><br>

<h2>Quem precisa dos cuidados paleativos?</h2><br>
<p>Os cuidados paliativos são indicados para todas as pessoas que sofrem de uma doença crónica avançada ou incurável, e em progressão, que ameaça a continuidade da vida, sendo, também, chamada de doença terminal. Alguns exemplos de situações em que se aplicam os cuidados paliativos, seja para qualquer faixa etária, incluem:</p>
<br>
<ul>
    <li id="list">Câncer;</li>
    <li id="list">Doenças degenerativas neurológicas, como Alzheimer, Parkinson, esclerose múltipla ou esclerose lateral amiotrófica;</li>
    <li id="list">Doenças crônicas, como artrite grave;</li>
    <li id="list">Doença renal ou hepática crônicas;</li>
    <li id="list">Cardiopatias terminais;</li>
    <li id="list">Problemas pulmonares crônicos;</li>
    <li id="list">AIDS avançada;</li>
    <li id="list">Traumatismo craniano grave ou AVC;</li>
    <li id="list">Coma irreversível;</li>
    <li id="list">Doenças genéticas ou doenças congênitas incuráveis;</li>
    <li id="list">Doenças pneumáticas;</li>
</ul><br>
<h2>Onde os cuidados paliativos são realizados?</h2><br>
<p>Os cuidados paliativos são oferecidos em hospitais ou em casa. Para cada etapa da doença e do tratamento, a equipe irá avaliar o melhor local para a assistência acontecer.</p><br>
<p>Na grande maioria, os pacientes preferem que os procedimentos sejam feitos em domicílio. Mas quem mora, por exemplo, em casas de repouso, pode receber cuidados paliativos nesse local. Nesses casos, essas instalações são consideradas atendimento domiciliar, uma vez que a instalação é o lar do paciente. Nessa configuração, o cuidador principal é o responsável pela supervisão e cuidado do paciente, embora exista uma equipe de enfermeiros, médicos e outros profissionais disponíveis.</p><br>
<p>Já no hospital, geralmente existem unidades de cuidados paliativos especiais, ou no mínimo uma equipe designada para a função. Nesse local, o paciente recebe atendimento 24 horas por dia no controle dos sintomas.</p><br>

<h2>Como se procedem os tratamentos?</h2><br>
<p>Os tratamentos são realizados por uma equipe interdisciplinar de cuidados paliativos, que supervisionam os aspectos de cuidados do paciente, 7 dias por semana e 24 horas por dia. Isso inclui supervisionar todos os profissionais que compartilham informações sobre o paciente, seja na internação, home care, médicos, farmacêuticos, clero, serviço funerário e a família.</p><br>
<p>Geralmente, em casa, o cuidador principal é um familiar ou amigo próximo, e é ele quem desenvolve na prática o programa de cuidados com base nas necessidades e preferências específicas do paciente e é ele também que manterá o contato com a equipe médica. Já no hospital, os profissionais que trabalham com cuidados paliativos formam uma equipe composta por médicos, enfermeiros, assistentes sociais, conselheiros, clero, terapeutas e voluntários treinados.</p><br>
